package com.bio.value;

public class FeedValue {
    Long base;
    SolutionValue solution;
    String apply;
    Long organism;
    String purpose;

    public Long getBase() {
        return base;
    }

    public void setBase(Long base) {
        this.base = base;
    }

    public SolutionValue getSolution() {
        return solution;
    }

    public void setSolution(SolutionValue solution) {
        this.solution = solution;
    }

    public String getApply() {
        return apply;
    }

    public void setApply(String apply) {
        this.apply = apply;
    }

    public Long getOrganism() {
        return organism;
    }

    public void setOrganism(Long organism) {
        this.organism = organism;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }
}
